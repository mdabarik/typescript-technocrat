// basic data type

// string 
let firstName : string = "barik";

// number
let roll: number = 123;

// boolean
let isAdmin : boolean = true;

// undefined
let x : undefined = undefined;

// null
let y : null = null;


// anytype if not value is defined (don't use it)
let d;
d = 123;
d = "ear";
d = true;

// array
let friends = ["barik", "monir", "don"];
let eligibleRollList : number[] = [1, 3]

// tuple --> array --> order --> type of values 
let coordinates : [number, number] = [1, 5];
let ageName : [number, string, boolean] = [26, "barik", true];
